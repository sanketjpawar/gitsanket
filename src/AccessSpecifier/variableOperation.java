package AccessSpecifier;

public class variableOperation {
	
	private int a = 14;
	int b = 85;
	protected int c = 921;
	public int d = 82;

	public static void main(String[] args) {
		variableOperation p1 = new variableOperation();
		
		System.out.println(" value of a is :"+p1.a);
		System.out.println(" value of b is :"+p1.b);
		System.out.println(" value of c is :"+p1.c);
		System.out.println(" value of d is :"+p1.d);
	}

}
