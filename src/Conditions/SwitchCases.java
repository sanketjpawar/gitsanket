package Conditions;

public class SwitchCases {

	public static void main(String[] args) {
		// Day1 to 7 Monday to Sunday

		int day = 4;
		switch (day)
		{
		case 1: System.out.println("Monday");
		break;
		case 2: System.out.println("Tuesday");
		break;
		case 3: System.out.println("Wednesday");
		break;
		case 4: System.out.println("Thursday");
		break;
		case 5: System.out.println("Friday");
		break;
		case 6: System.out.println("Saturday");
		break;
		case 7: System.out.println("Sunday");
		break;
		default : System.out.println("Please enter valid number");
		}
				
		String month = "Oct";
		switch (month)
		{
		case "Jan": System.out.println("This is 1st month of year");
		break;
		case "Feb": System.out.println("This is 2nd month of year");
		break;
		case "Mar": System.out.println("This is 3rd month of year");
		break;
		case "Apr": System.out.println("This is 4th month of year");
		break;
		case "May": System.out.println("This is 5th month of year");
		break;
		case "Jun": System.out.println("This is 6th month of year");
		break;
		case "Jul": System.out.println("This is 7th month of year");
		break;
		case "Aug": System.out.println("This is 8th month of year");
		break;
		case "Sept": System.out.println("This is 9th month of year");
		break;
		case "Oct": System.out.println("This is 10th month of year");
		break;
		case "Nov": System.out.println("This is 11th month of year");
		break;
		case "Dec": System.out.println("This is 12th month of year");
		break;
		default : System.out.println("Invalid input");
		}
		
		}
	}

